#include<reg52.h>  //通用89C52头文件
#define uint unsigned int 
#define uchar unsigned char


/*********************************************************************************************
函数名：毫秒级CPU延时函数
调  用：DELAY_MS (?);
参  数：1~65535（参数不可为0）
返回值：无
结  果：占用CPU方式延时与参数数值相同的毫秒时间
备  注：应用于1T单片机时i<600，应用于12T单片机时i<125
/*********************************************************************************************/

void DELAY_MS(uint a)
{
	uint i;
	while(a--!=0)
	{
		for(i=0;i<600;i++);
	}	
}

/*********************************************************************************************
函数名：主函数
调  用：无
参  数：无
返回值：无
结  果：程序开始处，无限循环
备  注：
/**********************************************************************************************/

void main(void)
{
	uchar cnt =0;
	P0=0x00;
	while(1)
	{
		for(;cnt<8;cnt++)
		{
			P0=(0x01<<cnt);
			DELAY_MS(100);
		}
		cnt=0;
		for(;cnt<8;cnt++)
		{
			P0=(0x01<<cnt);
			DELAY_MS(100);
		}
		cnt=0;

		
	}

}
